<h2>Tarsqi References</h2>

<div class="description">

<p>Why use an existing tool if you can reinvent the wheel?</p>

<p>These pages are based on something I whipped up when keeping track of references for my dissertation. It is a simple interface to add and browse references. Each reference contains the following pieces of information:</p>

<ol>
<li>title
<li>author
<li>year of publication
<li>list of keywords
<li>bibtex entry
<li>html representation of the reference
<li>notes (to add abstracts and such)
</ol>

<p>Everything can be edited. Edits will apparantly overwrite the old data, but please feel relieved knowing that the database stores snapshots of all previous versions of a reference. Notes cannot be edited, all you can do is add new ones. There is a set of keywords that I found useful, more can be added at will on the keywords page. The database is populated with tarsqi and llc related references, but it ain't complete, feel free to add missing nuggets of wisdom. So far, I have not added any html representations, assuming and hoping that there is a tool that will create them from bibtex entries.</p>

<p>In the future, I will add some limited functionality, such as the option to export bibtext and html entries. Please send email to me if you run into any bugs (not that there are any of course).</p>

<p>Marc Verhagen, January 2008.</p>

